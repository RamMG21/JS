//OPERADORES EN JAVA SCRIPT

//Operadores Aritmeticos
/*Los operadores aritmeticos toman valores numericos (ya sean literales o variables)
como sus operandos y retornan un valor numerico unico */

numero1 = 10;
numero2 = 5;

document.write("Resultados de operaciones Aritmeticas");

//Suma
resultado = numero1 + numero2;
document.write("\nSuma " + resultado);

//Decreremento
resultado = numero1--;
document.write("\nResta " + resultado);

