//Operadores de Asignacion
/* Un operador de asignacion asigna un valor al operando de la 
izquierda basado en el valor del operrando de la derecha*/

let numeroA = 10;
document.write("\nResultados de Operadores de Asignacion \n");
//suma
numeroA += 5;
  document.write(numeroA + " ");

//resta
numeroA -= 5;
  document.write(numeroA + " ");

  //Multiplicacion
numeroA *= 5;
  document.write(numeroA + " ");

  //Division
numeroA /= 5;
  document.write(numeroA + " ");

  //Residuo
numeroA %= 6;
  document.write(numeroA + " ");

  //Exponente
  numeroA **= 5;
  document.write(numeroA + " ");